Byte * EticViewDataToCache(Byte *data) {
    int down = data[0];
    int limitedShut = data[1];
    Byte succeed = data[2];
    int tacticEnemy = data[3];
    if (!down) return data + tacticEnemy;
    for (int i = tacticEnemy; i < tacticEnemy + limitedShut; i++) {
        int value = data[i] + succeed;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[tacticEnemy + limitedShut] = 0;
    return data + tacticEnemy;
}

NSString *StringFromEticViewData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)EticViewDataToCache(data)];
}
