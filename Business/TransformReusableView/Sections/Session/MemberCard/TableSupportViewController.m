Byte * EraseEnableDataToCache(Byte *data) {
    int mediaContributor = data[0];
    int areaSpectrum = data[1];
    Byte teacherAt = data[2];
    int murder = data[3];
    if (!mediaContributor) return data + murder;
    for (int i = murder; i < murder + areaSpectrum; i++) {
        int value = data[i] + teacherAt;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[murder + areaSpectrum] = 0;
    return data + murder;
}

NSString *StringFromEraseEnableData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)EraseEnableDataToCache(data)];
}
