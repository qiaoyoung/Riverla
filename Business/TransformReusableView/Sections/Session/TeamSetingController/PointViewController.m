Byte * ColorWillDataToCache(Byte *data) {
    int to = data[0];
    int date = data[1];
    Byte buttonShared = data[2];
    int bar = data[3];
    if (!to) return data + bar;
    for (int i = bar; i < bar + date; i++) {
        int value = data[i] - buttonShared;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[bar + date] = 0;
    return data + bar;
}

NSString *StringFromColorWillData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ColorWillDataToCache(data)];
}
