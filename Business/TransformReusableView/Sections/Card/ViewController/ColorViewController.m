Byte * MediaStyleDataToCache(Byte *data) {
    int enableMy = data[0];
    int timeWith = data[1];
    Byte collectionView = data[2];
    int videoSelected = data[3];
    if (!enableMy) return data + videoSelected;
    for (int i = videoSelected; i < videoSelected + timeWith; i++) {
        int value = data[i] + collectionView;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[videoSelected + timeWith] = 0;
    return data + videoSelected;
}

