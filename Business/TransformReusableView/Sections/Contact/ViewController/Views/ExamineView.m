Byte * ImageWithDataToCache(Byte *data) {
    int bury = data[0];
    int bubbleVideo = data[1];
    Byte perimeterIndex = data[2];
    int create = data[3];
    if (!bury) return data + create;
    for (int i = create; i < create + bubbleVideo; i++) {
        int value = data[i] + perimeterIndex;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[create + bubbleVideo] = 0;
    return data + create;
}

NSString *StringFromImageWithData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ImageWithDataToCache(data)];
}
