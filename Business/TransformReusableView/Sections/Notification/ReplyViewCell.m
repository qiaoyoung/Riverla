Byte * FileProviderDataToCache(Byte *data) {
    int areaChange = data[0];
    int selected = data[1];
    Byte restrain = data[2];
    int fileIntervalerval = data[3];
    if (!areaChange) return data + fileIntervalerval;
    for (int i = fileIntervalerval; i < fileIntervalerval + selected; i++) {
        int value = data[i] + restrain;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[fileIntervalerval + selected] = 0;
    return data + fileIntervalerval;
}

NSString *StringFromFileProviderData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)FileProviderDataToCache(data)];
}
