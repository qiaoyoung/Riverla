Byte * GoodHeDataToCache(Byte *data) {
    int fileBalloon = data[0];
    int leaAllowOxygen = data[1];
    int perfectCheat = data[2];
    if (!fileBalloon) return data + perfectCheat;
    for (int i = 0; i < leaAllowOxygen / 2; i++) {
        int begin = perfectCheat + i;
        int end = perfectCheat + leaAllowOxygen - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[perfectCheat + leaAllowOxygen] = 0;
    return data + perfectCheat;
}

NSString *StringFromGoodHeData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)GoodHeDataToCache(data)];
}  
