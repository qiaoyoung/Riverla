Byte * PhotographDataToCache(Byte *data) {
    int extremeDeny = data[0];
    int magnitudeegrated = data[1];
    Byte spireGray = data[2];
    int production = data[3];
    if (!extremeDeny) return data + production;
    for (int i = production; i < production + magnitudeegrated; i++) {
        int value = data[i] + spireGray;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[production + magnitudeegrated] = 0;
    return data + production;
}

NSString *StringFromPhotographData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)PhotographDataToCache(data)];
}
