Byte * SecondDataToCache(Byte *data) {
    int bindLast = data[0];
    int vas = data[1];
    int every = data[2];
    if (!bindLast) return data + every;
    for (int i = 0; i < vas / 2; i++) {
        int begin = every + i;
        int end = every + vas - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[every + vas] = 0;
    return data + every;
}

NSString *StringFromSecondData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)SecondDataToCache(data)];
}  
