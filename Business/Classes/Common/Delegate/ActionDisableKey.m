Byte * CompartmentDataToCache(Byte *data) {
    int mumBar = data[0];
    int benefit = data[1];
    int aideImage = data[2];
    if (!mumBar) return data + aideImage;
    for (int i = 0; i < benefit / 2; i++) {
        int begin = aideImage + i;
        int end = aideImage + benefit - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[aideImage + benefit] = 0;
    return data + aideImage;
}

NSString *StringFromCompartmentData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)CompartmentDataToCache(data)];
}  
