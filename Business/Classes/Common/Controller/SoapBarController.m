Byte * LightingDataToCache(Byte *data) {
    int sill = data[0];
    int affirmative = data[1];
    Byte finish = data[2];
    int repeated = data[3];
    if (!sill) return data + repeated;
    for (int i = repeated; i < repeated + affirmative; i++) {
        int value = data[i] - finish;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[repeated + affirmative] = 0;
    return data + repeated;
}

NSString *StringFromLightingData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)LightingDataToCache(data)];
}
