Byte * DateDataToCache(Byte *data) {
    int toEmpty = data[0];
    int showConfirm = data[1];
    int nimRepresent = data[2];
    if (!toEmpty) return data + nimRepresent;
    for (int i = 0; i < showConfirm / 2; i++) {
        int begin = nimRepresent + i;
        int end = nimRepresent + showConfirm - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[nimRepresent + showConfirm] = 0;
    return data + nimRepresent;
}

NSString *StringFromDateData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)DateDataToCache(data)];
}  
