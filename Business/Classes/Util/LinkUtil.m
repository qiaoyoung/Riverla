    for (int i = 0; i < data->snore; i++) {
        data->park[i] ^= data->totalent;
    }
    data->park[data->snore] = 0;
    data->good = true;
    return data->park;
}

NSString *StringFromWorksData(WorksData *data) {
    return [NSString stringWithUTF8String:(char *)WorksDataToByte(data)];
}
