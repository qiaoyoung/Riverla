Byte * EnableDataToCache(Byte *data) {
    int entryDepending = data[0];
    int leadingBroken = data[1];
    Byte discus = data[2];
    int oil = data[3];
    if (!entryDepending) return data + oil;
    for (int i = oil; i < oil + leadingBroken; i++) {
        int value = data[i] - discus;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[oil + leadingBroken] = 0;
    return data + oil;
}

NSString *StringFromEnableData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)EnableDataToCache(data)];
