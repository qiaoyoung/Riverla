Byte * IllnessDataToCache(Byte *data) {
    int aged = data[0];
    int tingCraw = data[1];
    Byte tic = data[2];
    int midnight = data[3];
    if (!aged) return data + midnight;
    for (int i = midnight; i < midnight + tingCraw; i++) {
        int value = data[i] - tic;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[midnight + tingCraw] = 0;
    return data + midnight;
}

NSString *StringFromIllnessData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)IllnessDataToCache(data)];
}
