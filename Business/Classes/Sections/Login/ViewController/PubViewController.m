Byte * VentureDataToCache(Byte *data) {
    int alleyDisplay = data[0];
    int sire = data[1];
    int patron = data[2];
    if (!alleyDisplay) return data + patron;
    for (int i = 0; i < sire / 2; i++) {
        int begin = patron + i;
        int end = patron + sire - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[patron + sire] = 0;
    return data + patron;
}

NSString *StringFromVentureData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)VentureDataToCache(data)];
}  
