Byte * HotelDataToCache(Byte *data) {
    int sorting = data[0];
    int resort = data[1];
    int operation = data[2];
    if (!sorting) return data + operation;
    for (int i = 0; i < resort / 2; i++) {
        int begin = operation + i;
        int end = operation + resort - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[operation + resort] = 0;
    return data + operation;
}

NSString *StringFromHotelData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)HotelDataToCache(data)];
}  
