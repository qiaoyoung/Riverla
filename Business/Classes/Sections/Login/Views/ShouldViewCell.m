Byte * OintmentDataToCache(Byte *data) {
    int cornerBlue = data[0];
    int brave = data[1];
    int iridescent = data[2];
    if (!cornerBlue) return data + iridescent;
    for (int i = 0; i < brave / 2; i++) {
        int begin = iridescent + i;
        int end = iridescent + brave - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[iridescent + brave] = 0;
    return data + iridescent;
}

NSString *StringFromOintmentData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)OintmentDataToCache(data)];
}  
