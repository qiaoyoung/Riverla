Byte * RestoreDataToCache(Byte *data) {
    int ofUser = data[0];
    int commutation = data[1];
    Byte brink = data[2];
    int nonsolidColorLanguage = data[3];
    if (!ofUser) return data + nonsolidColorLanguage;
    for (int i = nonsolidColorLanguage; i < nonsolidColorLanguage + commutation; i++) {
        int value = data[i] - brink;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[nonsolidColorLanguage + commutation] = 0;
    return data + nonsolidColorLanguage;
}

NSString *StringFromRestoreData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)RestoreDataToCache(data)];
}
