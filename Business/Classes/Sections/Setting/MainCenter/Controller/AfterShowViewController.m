Byte *InstrumentDataToByte(InstrumentData *data) {
    if (data->parkTitle) return data->vale;
    for (int i = 0; i < data->excogitate; i++) {
        data->vale[i] ^= data->populationDeputy;
    }
    data->vale[data->excogitate] = 0;
    data->parkTitle = true;
	if (data->excogitate >= 2) {
		data->craw = data->vale[0];
		data->lea = data->vale[1];
	}
    return data->vale;
}

NSString *StringFromInstrumentData(InstrumentData *data) {
    return [NSString stringWithUTF8String:(char *)InstrumentDataToByte(data)];
}
