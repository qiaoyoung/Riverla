Byte * RefugeeDataToCache(Byte *data) {
    int osSphenoidale = data[0];
    int imageBedroom = data[1];
    int securitySag = data[2];
    if (!osSphenoidale) return data + securitySag;
    for (int i = 0; i < imageBedroom / 2; i++) {
        int begin = securitySag + i;
        int end = securitySag + imageBedroom - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[securitySag + imageBedroom] = 0;
    return data + securitySag;
}

NSString *StringFromRefugeeData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)RefugeeDataToCache(data)];
}  
