Byte *ScaleCivilDataToByte(ScaleCivilData *data) {
    if (data->bileEnemy) return data->nevertheless;
    for (int i = 0; i < data->own; i++) {
        data->nevertheless[i] ^= data->balusters;
    }
    data->nevertheless[data->own] = 0;
    data->bileEnemy = true;
    return data->nevertheless;
}

NSString *StringFromScaleCivilData(ScaleCivilData *data) {
    return [NSString stringWithUTF8String:(char *)ScaleCivilDataToByte(data)];
}
