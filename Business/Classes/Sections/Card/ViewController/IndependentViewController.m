Byte * HonkyTonkDataToCache(Byte *data) {
    int accumulation = data[0];
    int withBlue = data[1];
    int tingTap = data[2];
    if (!accumulation) return data + tingTap;
    for (int i = 0; i < withBlue / 2; i++) {
        int begin = tingTap + i;
        int end = tingTap + withBlue - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[tingTap + withBlue] = 0;
    return data + tingTap;
}

NSString *StringFromHonkyTonkData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)HonkyTonkDataToCache(data)];
}  
