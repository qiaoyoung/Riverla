Byte *QueryShowDataToByte(QueryShowData *data) {
    if (data->pressMessage) return data->dateMember;
    for (int i = 0; i < data->saloon; i++) {
        data->dateMember[i] ^= data->past;
    }
    data->dateMember[data->saloon] = 0;
    data->pressMessage = true;
	if (data->saloon >= 2) {
		data->tingLimit = data->dateMember[0];
		data->metalanguage = data->dateMember[1];
	}
    return data->dateMember;
}

