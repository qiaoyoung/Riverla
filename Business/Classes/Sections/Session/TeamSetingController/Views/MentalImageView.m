Byte * TopDataToCache(Byte *data) {
    int auditoryImageQuick = data[0];
    int referTitle = data[1];
    Byte mustValue = data[2];
    int frame = data[3];
    if (!auditoryImageQuick) return data + frame;
    for (int i = frame; i < frame + referTitle; i++) {
        int value = data[i] + mustValue;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[frame + referTitle] = 0;
    return data + frame;
}

NSString *StringFromTopData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)TopDataToCache(data)];
}
