Byte * DestructionDataToCache(Byte *data) {
    int bypassCompose = data[0];
    int roach = data[1];
    Byte skilledWorkman = data[2];
    int wordOfAdvice = data[3];
    if (!bypassCompose) return data + wordOfAdvice;
    for (int i = wordOfAdvice; i < wordOfAdvice + roach; i++) {
        int value = data[i] - skilledWorkman;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[wordOfAdvice + roach] = 0;
    return data + wordOfAdvice;
}

NSString *StringFromDestructionData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)DestructionDataToCache(data)];
}
