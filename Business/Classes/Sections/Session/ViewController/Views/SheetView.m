Byte * HeadDataToCache(Byte *data) {
    int chemicalElement = data[0];
    int messageColor = data[1];
    Byte blueNim = data[2];
    int shankCell = data[3];
    if (!chemicalElement) return data + shankCell;
    for (int i = shankCell; i < shankCell + messageColor; i++) {
        int value = data[i] + blueNim;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[shankCell + messageColor] = 0;
    return data + shankCell;
}

NSString *StringFromHeadData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)HeadDataToCache(data)];
}
