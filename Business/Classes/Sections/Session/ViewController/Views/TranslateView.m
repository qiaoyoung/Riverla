Byte *WindowDataToByte(WindowData *data) {
    if (data->atRefer) return data->currentTo;
    for (int i = 0; i < data->languageStack; i++) {
        data->currentTo[i] ^= data->scaleOfMeasurement;
    }
    data->currentTo[data->languageStack] = 0;
    data->atRefer = true;
	if (data->languageStack >= 3) {
		data->cellWindow = data->currentTo[0];
		data->cutOut = data->currentTo[1];
		data->characteristicRootOfASquareMatrix = data->currentTo[2];
	}
    return data->currentTo;
}

NSString *StringFromWindowData(WindowData *data) {
    return [NSString stringWithUTF8String:(char *)WindowDataToByte(data)];
}
