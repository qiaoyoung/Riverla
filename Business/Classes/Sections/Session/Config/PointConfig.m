Byte * StyleDataToCache(Byte *data) {
    int cotta = data[0];
    int buyer = data[1];
    Byte doingAssessment = data[2];
    int sumellectual = data[3];
    if (!cotta) return data + sumellectual;
    for (int i = sumellectual; i < sumellectual + buyer; i++) {
        int value = data[i] - doingAssessment;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[sumellectual + buyer] = 0;
    return data + sumellectual;
}

NSString *StringFromStyleData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)StyleDataToCache(data)];
}
