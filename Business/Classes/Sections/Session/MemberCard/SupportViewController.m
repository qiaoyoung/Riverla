Byte * GalaTacticDataToCache(Byte *data) {
    int equate = data[0];
    int birdSEyeView = data[1];
    int angel = data[2];
    if (!equate) return data + angel;
    for (int i = 0; i < birdSEyeView / 2; i++) {
        int begin = angel + i;
        int end = angel + birdSEyeView - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[angel + birdSEyeView] = 0;
    return data + angel;
}

NSString *StringFromGalaTacticData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)GalaTacticDataToCache(data)];
}  
