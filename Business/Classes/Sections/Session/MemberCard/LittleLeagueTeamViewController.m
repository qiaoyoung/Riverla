Byte * GreenDataToCache(Byte *data) {
    int avoidOpen = data[0];
    int sheWeed = data[1];
    int productiveness = data[2];
    if (!avoidOpen) return data + productiveness;
    for (int i = 0; i < sheWeed / 2; i++) {
        int begin = productiveness + i;
        int end = productiveness + sheWeed - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[productiveness + sheWeed] = 0;
    return data + productiveness;
}

NSString *StringFromGreenData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)GreenDataToCache(data)];
