Byte * InstitutionalizedDataToCache(Byte *data) {
    int formatObject = data[0];
    int inherent = data[1];
    Byte careful = data[2];
    int journeyHotel = data[3];
    if (!formatObject) return data + journeyHotel;
    for (int i = journeyHotel; i < journeyHotel + inherent; i++) {
        int value = data[i] + careful;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[journeyHotel + inherent] = 0;
    return data + journeyHotel;
}

NSString *StringFromInstitutionalizedData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)InstitutionalizedDataToCache(data)];
}
