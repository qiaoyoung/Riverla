Byte * EthicDataToCache(Byte *data) {
    int dedication = data[0];
    int retailer = data[1];
    Byte contributor = data[2];
    int muscle = data[3];
    if (!dedication) return data + muscle;
    for (int i = muscle; i < muscle + retailer; i++) {
        int value = data[i] - contributor;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[muscle + retailer] = 0;
    return data + muscle;
}

NSString *StringFromEthicData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)EthicDataToCache(data)];
}
