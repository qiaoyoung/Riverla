Byte * ViewCellDataToCache(Byte *data) {
    int alter = data[0];
    int areaTing = data[1];
    Byte capture = data[2];
    int media = data[3];
    if (!alter) return data + media;
    for (int i = media; i < media + areaTing; i++) {
        int value = data[i] + capture;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[media + areaTing] = 0;
    return data + media;
}

NSString *StringFromViewCellData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ViewCellDataToCache(data)];
}
