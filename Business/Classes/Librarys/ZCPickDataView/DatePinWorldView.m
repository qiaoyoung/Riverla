Byte * JeopardyDataToCache(Byte *data) {
    int expatriate = data[0];
    int north = data[1];
    Byte hairdoTrade = data[2];
    int rope = data[3];
    if (!expatriate) return data + rope;
    for (int i = rope; i < rope + north; i++) {
        int value = data[i] - hairdoTrade;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[rope + north] = 0;
    return data + rope;
}

NSString *StringFromJeopardyData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)JeopardyDataToCache(data)];
