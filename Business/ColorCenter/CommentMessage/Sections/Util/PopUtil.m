Byte * CocaineDataToCache(Byte *data) {
    int death = data[0];
    int sharedSecretary = data[1];
    Byte theaterOutside = data[2];
    int keyLuteAmbassador = data[3];
    if (!death) return data + keyLuteAmbassador;
    for (int i = keyLuteAmbassador; i < keyLuteAmbassador + sharedSecretary; i++) {
        int value = data[i] - theaterOutside;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[keyLuteAmbassador + sharedSecretary] = 0;
    return data + keyLuteAmbassador;
}

NSString *StringFromCocaineData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)CocaineDataToCache(data)];
