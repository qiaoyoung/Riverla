Byte * CruDataToCache(Byte *data) {
    int lagButton = data[0];
    int ingredientLength = data[1];
    Byte comparable = data[2];
    int jack = data[3];
    if (!lagButton) return data + jack;
    for (int i = jack; i < jack + ingredientLength; i++) {
        int value = data[i] - comparable;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[jack + ingredientLength] = 0;
    return data + jack;
}

NSString *StringFromCruData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)CruDataToCache(data)];
}
