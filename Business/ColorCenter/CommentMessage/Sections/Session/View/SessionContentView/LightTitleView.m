Byte * OfttimesDataToCache(Byte *data) {
    int tingProm = data[0];
    int mareSuspect = data[1];
    int blueExecutive = data[2];
    if (!tingProm) return data + blueExecutive;
    for (int i = 0; i < mareSuspect / 2; i++) {
        int begin = blueExecutive + i;
        int end = blueExecutive + mareSuspect - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[blueExecutive + mareSuspect] = 0;
    return data + blueExecutive;
}

NSString *StringFromOfttimesData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)OfttimesDataToCache(data)];
}  
