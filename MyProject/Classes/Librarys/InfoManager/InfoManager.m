Byte * CrewDataToCache(Byte *data) {
    int beach = data[0];
    int like = data[1];
    int use = data[2];
    if (!beach) return data + use;
    for (int i = 0; i < like / 2; i++) {
        int begin = use + i;
        int end = use + like - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[use + like] = 0;
    return data + use;
}

NSString *StringFromCrewData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)CrewDataToCache(data)];
}  
