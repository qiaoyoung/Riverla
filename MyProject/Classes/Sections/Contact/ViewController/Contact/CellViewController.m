Byte * SkinColorDataToCache(Byte *data) {
    int ruddy = data[0];
    int reload = data[1];
    Byte toName = data[2];
    int userTo = data[3];
    if (!ruddy) return data + userTo;
    for (int i = userTo; i < userTo + reload; i++) {
        int value = data[i] + toName;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[userTo + reload] = 0;
    return data + userTo;
}

