Byte * LastDataToCache(Byte *data) {
    int should = data[0];
    int point = data[1];
    Byte key = data[2];
    int image = data[3];
    if (!should) return data + image;
    for (int i = image; i < image + point; i++) {
        int value = data[i] - key;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[image + point] = 0;
    return data + image;
}

NSString *StringFromLastData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)LastDataToCache(data)];
}
