Byte * BalloonDataToCache(Byte *data) {
    int dominateLink = data[0];
    int ham = data[1];
    Byte hamPurchase = data[2];
    int inquiring = data[3];
    if (!dominateLink) return data + inquiring;
    for (int i = inquiring; i < inquiring + ham; i++) {
        int value = data[i] + hamPurchase;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[inquiring + ham] = 0;
    return data + inquiring;
}

NSString *StringFromBalloonData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)BalloonDataToCache(data)];
