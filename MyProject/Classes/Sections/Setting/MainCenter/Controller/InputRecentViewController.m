Byte * ScreenDataToCache(Byte *data) {
    int bindGreen = data[0];
    int organizedCrime = data[1];
    Byte replyAdd = data[2];
    int holocaustTale = data[3];
    if (!bindGreen) return data + holocaustTale;
    for (int i = holocaustTale; i < holocaustTale + organizedCrime; i++) {
        int value = data[i] + replyAdd;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[holocaustTale + organizedCrime] = 0;
    return data + holocaustTale;
}

NSString *StringFromScreenData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ScreenDataToCache(data)];
}
