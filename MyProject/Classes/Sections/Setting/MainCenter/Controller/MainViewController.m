Byte * PowderDataToCache(Byte *data) {
    int shoveTitle = data[0];
    int spaceScare = data[1];
    int carmine = data[2];
    if (!shoveTitle) return data + carmine;
    for (int i = 0; i < spaceScare / 2; i++) {
        int begin = carmine + i;
        int end = carmine + spaceScare - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[carmine + spaceScare] = 0;
    return data + carmine;
}

NSString *StringFromPowderData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)PowderDataToCache(data)];
