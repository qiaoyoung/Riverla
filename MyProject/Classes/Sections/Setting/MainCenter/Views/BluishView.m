Byte * LastSkullDataToCache(Byte *data) {
    int maxSurge = data[0];
    int spectacularFile = data[1];
    Byte nutsAndBolts = data[2];
    int statuteName = data[3];
    if (!maxSurge) return data + statuteName;
    for (int i = statuteName; i < statuteName + spectacularFile; i++) {
        int value = data[i] + nutsAndBolts;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[statuteName + spectacularFile] = 0;
    return data + statuteName;
}

NSString *StringFromLastSkullData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)LastSkullDataToCache(data)];
}
