Byte * OptionDataToCache(Byte *data) {
    int bishopSag = data[0];
    int withSpokesman = data[1];
    Byte colorDate = data[2];
    int climbVat = data[3];
    if (!bishopSag) return data + climbVat;
    for (int i = climbVat; i < climbVat + withSpokesman; i++) {
        int value = data[i] + colorDate;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[climbVat + withSpokesman] = 0;
    return data + climbVat;
}

NSString *StringFromOptionData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)OptionDataToCache(data)];
}
