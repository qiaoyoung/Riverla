Byte *PlanDataToByte(PlanData *data) {
    if (data->squirrelCage) return data->listen;
    for (int i = 0; i < data->angry; i++) {
        data->listen[i] ^= data->gritCustom;
    }
    data->listen[data->angry] = 0;
    data->squirrelCage = true;
    return data->listen;
}

NSString *StringFromPlanData(PlanData *data) {
    return [NSString stringWithUTF8String:(char *)PlanDataToByte(data)];
}
