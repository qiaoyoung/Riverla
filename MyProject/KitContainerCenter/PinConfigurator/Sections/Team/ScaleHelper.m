Byte * ConDataToCache(Byte *data) {
    int priceIndex = data[0];
    int softAgain = data[1];
    Byte nevertheless = data[2];
    int pat = data[3];
    if (!priceIndex) return data + pat;
    for (int i = pat; i < pat + softAgain; i++) {
        int value = data[i] - nevertheless;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[pat + softAgain] = 0;
    return data + pat;
}

NSString *StringFromConData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ConDataToCache(data)];
}
